/* ============================== finger ================================ */
void finger(UI pos_x, UI pos_y, UC color)
{
 UC i,k;
 UC dot_byte;
 static UC bit_map[8]={0x80,0x40,0x20,0x10,0x08,0x04,0x02,0x01};

 set_dot_mode(color);
 for (i=0;i<48;i++)
 {
	dot_byte = finger_dot[i];
	for (k=0;k<8;k++)
	{
		if ((dot_byte & bit_map[k]) != 0)
			pixel(pos_x,pos_y);
		pos_x++;
	}
	if ( i&0x01 )
	{	pos_y++;
		pos_x -= 16;
	}
 }
 reset_dot_mode();
}

/* ============================== h_finger ================================ */
void h_finger(UI pos_x, UI pos_y, UC color)
{
UC i,k;
UC dot_byte;
UC count=3;
static UC bit_map[8]={0x80,0x40,0x20,0x10,0x08,0x04,0x02,0x01};

 set_dot_mode(color);
 pos_y++;
 for (i=3; i<45; i++)
 {
	dot_byte = h_finger_dot[i];
	for (k=0;k<8;k++)
	{
		if ((dot_byte & bit_map[k]) != 0)
			pixel(pos_x,pos_y);
		pos_x++;
	}
	if ( --count==0 )
	{	pos_y++;
		pos_x -= 24;
		count = 3;
	}
 }
 reset_dot_mode();
}

